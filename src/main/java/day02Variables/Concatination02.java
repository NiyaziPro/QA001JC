package day02Variables;

public class Concatination02 {
    public static void main(String[] args) {
        String ifade1 = "Ali Can";
        char ifade2 = '?';
        String ifade3 = " topu bana ";
        String ifade4=" atar mısın";

        // Yukarıdaki ifadelerle anlamlı bir cumle olusturalım

        System.out.println(ifade1 + ifade3 + ifade4 + ifade2); // Ali Can topu bana  atar mısın?
    }
}
